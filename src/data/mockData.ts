const makeItem = (
  orcId: string,
  idx: number,
  nome: string,
  codigo: string,
  qtd: number,
  unidade: string,
  preco: number,
  desconto = 0
): OrcamentoItem => {
  const precoFinal = preco * (1 - desconto / 100);
  return {
    id: `item-${orcId}-${idx}`,
    orcamento_id: orcId,
    produto_id: `prod-${idx.toString().padStart(3, '0')}`,
    produto_nome: nome,
    produto_codigo: codigo,
    quantidade: qtd,
    unidade,
    preco_unitario: preco,
    desconto_percentual: desconto,
    preco_final: precoFinal,
    subtotal: precoFinal * qtd,
  };
};

